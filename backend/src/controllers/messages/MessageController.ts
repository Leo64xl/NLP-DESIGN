function generateUUID(): string {
    return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function(c) {
        const r = Math.random() * 16 | 0;
        const v = c === 'x' ? r : (r & 0x3 | 0x8);
        return v.toString(16);
    });
}

//  FUNCIÓN PARA MAPEAR BUILDING TYPE A DESIGN TYPE
function mapBuildingTypeToDesignType(buildingType: BuildingType): '2d' | '3d' | 'both' {
    switch (buildingType) {
        case '2d':
            return '2d';
        case '3d':
            return '3d';
        case 'both':
            return 'both';
        default:
            return '2d'; // fallback
    }
}

