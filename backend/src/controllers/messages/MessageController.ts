async function analyzeArchitecturalRequest(content: string): Promise<ArchitecturalAnalysis> {
  const lowerContent = content.toLowerCase();
  
  const areaMatch = content.match(/(\d+)\s*m[²2]?/i);
  const totalArea = areaMatch ? parseInt(areaMatch[1]) : 0;

  const rooms: Room[] = extractRoomsFromText(content);
  const specialFeatures: string[] = extractSpecialFeatures(content);
  const buildingType = determineBuildingType(content);
  const complexity = evaluateComplexity(totalArea, rooms, specialFeatures);
  const shouldGenerate = canAutoGenerate(totalArea, rooms, complexity);

  return {
    requirements: {
      totalArea,
      rooms,
      specialFeatures,
      style: extractStyle(content),
      budget: extractBudget(content)
    },
    designParameters: {
      buildingType,
      complexity,
      priority: 'quality'
    },
    autoGeneration: {
      shouldGenerate,
      suggestedFormats: shouldGenerate ? ['PDF', 'DWG', 'OBJ', 'JPG'] : ['PDF'],
      estimatedTime: calculateEstimatedTime(complexity, shouldGenerate)
    }
  };
}

async function generateArchitecturalResponse(
  analysis: ArchitecturalAnalysis, 
  design: any
): Promise<AIArchitecturalResponse> {
  
  const { requirements, designParameters, autoGeneration } = analysis;
  
  let designConcept = `🏠 **Análisis de tu proyecto: "${design.title}"**\n\n`;
  
  designConcept += `📐 **Especificaciones detectadas:**\n`;
  designConcept += `• Área total: ${requirements.totalArea}m²\n`;
  designConcept += `• Habitaciones: ${requirements.rooms.map((r: Room) => `${r.count} ${r.type}(s)`).join(', ')}\n`;
  
  if (requirements.specialFeatures.length > 0) {
    designConcept += `• Características especiales: ${requirements.specialFeatures.join(', ')}\n`;
  }
  
  designConcept += `\n🎯 **Propuesta arquitectónica:**\n`;
  
  if (requirements.totalArea > 0 && requirements.rooms.length > 0) {
    designConcept += generateLayoutProposal(requirements);
    designConcept += `\n📋 **Especificaciones técnicas:**\n`;
    designConcept += generateTechnicalSpecs(requirements);
  }

  if (autoGeneration.shouldGenerate) {
    designConcept += `\n🚀 **Generación automática iniciada:**\n`;
    designConcept += `• Planos 2D en formato PDF y DWG\n`;
    designConcept += `• Modelo 3D en formato OBJ\n`;
    designConcept += `• Renders fotorrealistas en JPG\n`;
    designConcept += `• Tiempo estimado: ${autoGeneration.estimatedTime}\n`;
  } else {
    designConcept += `\n💬 **Necesito más información para generar los planos:**\n`;
    designConcept += `• ¿Prefieres un estilo arquitectónico específico?\n`;
    designConcept += `• ¿Hay restricciones del terreno?\n`;
    designConcept += `• ¿Qué presupuesto aproximado manejas?\n`;
  }

  const autoGeneratedFiles = autoGeneration.shouldGenerate ? [
    { type: '2d', format: 'PDF', status: 'generating' as const, estimatedTime: '2-3 min' },
    { type: '2d', format: 'DWG', status: 'queued' as const, estimatedTime: '3-4 min' },
    { type: '3d', format: 'OBJ', status: 'queued' as const, estimatedTime: '5-8 min' },
    { type: 'render', format: 'JPG', status: 'queued' as const, estimatedTime: '8-12 min' }
  ] : [];

  return {
    analysis,
    designConcept,
    technicalSpecs: {
      dimensions: generateDimensions(requirements),
      layout: generateLayout(requirements),
      structuralNotes: generateStructuralNotes(requirements)
    },
    autoGeneratedFiles,
    conversationFlow: {
      nextSteps: autoGeneration.shouldGenerate ? 
        ['wait_for_generation', 'review_files', 'request_modifications'] :
        ['provide_more_details', 'confirm_requirements', 'start_generation'],
      clarificationQuestions: generateClarificationQuestions(requirements),
      readyForGeneration: autoGeneration.shouldGenerate
    }
  };
}

