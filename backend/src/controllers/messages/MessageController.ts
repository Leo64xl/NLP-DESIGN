type ArchitecturalFileType = '2d' | '3d' | 'render' | 'document'; 
type BuildingType = '2d' | '3d' | 'both';
type Complexity = 'simple' | 'medium' | 'complex';

interface Room {
  type: string;
  count: number;
  minArea?: number;
}

function getModelFileType(architecturalType: ArchitecturalFileType, format: string): ModelFileType {
  const typeMapping: Record<ArchitecturalFileType, Record<string, ModelFileType>> = {
    '2d': {
      'PDF': 'pdf',
      'DWG': 'dwg',
      'DXF': 'dxf',
      'JPG': 'image',
      'PNG': 'image'
    },
    '3d': {
      'OBJ': 'obj',
      'FBX': 'fbx',
      'GLTF': 'gltf',
      'SKP': 'skp'
    },
    'render': {
      'JPG': 'image',
      'PNG': 'image'
    },
    'document': {
      'PDF': 'pdf',
      'JSON': 'json'
    }
  };
  
  return typeMapping[architecturalType]?.[format] || 'pdf';
}

